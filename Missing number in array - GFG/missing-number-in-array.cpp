class Solution{
  public:
    int missingNumber(vector<int>& arr, int n) {
        // Your code goes here
        int ans = 0;
        for(int j =1;j<=n;j++)
          ans = ans ^ (j);
        for(int j =0;j<n-1;j++)
          ans = ans ^ arr[j];
      return ans;    
    }
};

